export const Icon = ({ name, size = 50, color = 'black', style }) => {
  const icon = icons[name];

  if (!icon) {
    return null;
  }

  const { width = size, height = size, viewBox, data, defs } = icon;

  const isSinglePath = data && 'd' in data;

  return (
    <Svg
      width={width}
      height={height}
      viewBox={viewBox}
      fill={color}
      style={style}
    >
      {defs && defs.linearGradients && (
        <Defs>
          {Object.keys(defs.linearGradients).map((key) => {
            const gradient = defs.linearGradients[key];
            return (
              <LinearGradient
                key={key}
                id={key}
                x1={gradient.x1}
                y1={gradient.y1}
                x2={gradient.x2}
                y2={gradient.y2}
                gradientUnits={gradient.gradientUnits || "userSpaceOnUse"}
              >
                {gradient.stops.map((stop, index) => (
                  <Stop
                    key={index}
                    offset={stop.offset}
                    stopColor={stop['stop-color']}
                    stopOpacity={stop['stop-opacity']}
                  />
                ))}
              </LinearGradient>
            );
          })}
        </Defs>
      )}
      
      {isSinglePath ? (
        <Path {...data} />
      ) : (
        data && data.paths && data.paths.map((path, index) => (
          <Path key={index} {...path} />
        ))
      )}
    </Svg>
  );
};

